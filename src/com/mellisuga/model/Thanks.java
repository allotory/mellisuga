package com.mellisuga.model;

public class Thanks {

	private int id;
	
	private int answer_id;
	
	private int thanker_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getAnswer_id() {
		return answer_id;
	}

	public void setAnswer_id(int answer_id) {
		this.answer_id = answer_id;
	}

	public int getThanker_id() {
		return thanker_id;
	}

	public void setThanker_id(int thanker_id) {
		this.thanker_id = thanker_id;
	}
	
}
